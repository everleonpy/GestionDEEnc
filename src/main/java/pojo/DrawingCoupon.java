package pojo;

public class DrawingCoupon {
	private String orgName;
	private String drawingName;
	private java.util.Date drawingDate;
	private java.util.Date couponDate;
	private String transactionNo;
	private String couponNo;
	private String customerNo;
	private String customerName;
	private String customerPhone;
	
	public String getOrgName() {
		return orgName;
	}
	public void setOrgName(String orgName) {
		this.orgName = orgName;
	}
	public String getDrawingName() {
		return drawingName;
	}
	public void setDrawingName(String drawingName) {
		this.drawingName = drawingName;
	}
	public java.util.Date getDrawingDate() {
		return drawingDate;
	}
	public void setDrawingDate(java.util.Date drawingDate) {
		this.drawingDate = drawingDate;
	}
	public java.util.Date getCouponDate() {
		return couponDate;
	}
	public void setCouponDate(java.util.Date couponDate) {
		this.couponDate = couponDate;
	}
	public String getTransactionNo() {
		return transactionNo;
	}
	public void setTransactionNo(String transactionNo) {
		this.transactionNo = transactionNo;
	}
	public String getCouponNo() {
		return couponNo;
	}
	public void setCouponNo(String couponNo) {
		this.couponNo = couponNo;
	}
	public String getCustomerNo() {
		return customerNo;
	}
	public void setCustomerNo(String customerNo) {
		this.customerNo = customerNo;
	}
	public String getCustomerName() {
		return customerName;
	}
	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}
	public String getCustomerPhone() {
		return customerPhone;
	}
	public void setCustomerPhone(String customerPhone) {
		this.customerPhone = customerPhone;
	}
}
